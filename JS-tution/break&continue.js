// break in loop*******************************
let i=1;
for(i=1;i<=5;i++)
{ 
    if(i==4)
    {
        break; // exit loop when i==4
    }
    console.log(i);
}


// continue in loop******************************************
for(let i=1; i<=5;i++){
    if(i==3)
    {
        continue; // skip 3
    }

    console.log(i);
} 
